// Get platform configuration by key
export const getPlatform = (key) => {
  return socialPlatforms.find(platform => platform.key === key);
};

// Validate social media URL
export const validateSocialUrl = (platform, url) => {
  if (!url) return { valid: true, message: '' };
  
  const platformConfig = getPlatform(platform);
  if (!platformConfig) return { valid: false, message: 'Unknown platform' };
  
  const regex = new RegExp(platformConfig.urlPattern, 'i');
  if (!regex.test(url)) {
    return { 
      valid: false, 
      message: `Please enter a valid ${platformConfig.name} URL` 
    };
  }
  
  return { valid: true, message: '' };
};

// Get social media icon component
export const getSocialIcon = (platform, className = "w-5 h-5") => {
  const platformConfig = getPlatform(platform);
  if (!platformConfig) return null;
  
  return React.cloneElement(platformConfig.icon, { 
    className: `${className} fill-current`,
    style: { color: platformConfig.color }
  });
};

// Generate social link with proper tracking
export const createSocialLink = (platform, url, username = '') => {
  if (!url) return null;
  
  const platformConfig = getPlatform(platform);
  if (!platformConfig) return null;
  
  return {
    platform: platformConfig.name,
    url,
    color: platformConfig.color,
    icon: platformConfig.icon,
    emoji: platformConfig.emoji,
    // Add utm tracking for analytics
    trackingUrl: `${url}?utm_source=irtzalink&utm_medium=profile&utm_campaign=${username || 'profile'}`
  };
};

const socialIconsUtils = {
  socialPlatforms,
  getPlatform,
  validateSocialUrl,
  getSocialIcon,
  createSocialLink
};

export default socialIconsUtils;
